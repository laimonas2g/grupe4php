<!-- Laravel “Autoservisas”


Sukurti web platformą, kurioje klientai galėtų užsisakyti autoserviso paslaugas.

Platforma sudaro vieša ir admin sritis. Į admin sritį gali patekti tik vartotojai turintys “admin” rolę, o į viešą visi platformoje užsiregistravę vartotojai.

Administracinė sritis.

1. Galimybė peržiūrėti, pridėti, ištrinti ir redaguoti autoservisą (adresas, pavadinimas, telefono numeris).
2. Galimybė peržiūrėti, pridėti, ištrinti ir redaguoti paslaugą (pavadinimas, trukmė, kaina).
3. Galimybė peržiūrėti, pridėti, ištrinti ir redaguoti mechaniką (vardas, pavardė, nuorauka, autoservisas kuriame dirba (iš 1 punkto sąrašo)). Galimybė matyti mechaniko įveritinimą.
4. Galimybė peržiūrėti, patvirtinti arba atšaukti, redaguoti datą arba ištrinti vartotojų užsakymus.


Vieša sritis

1. Kiekvienas norintis gali užsiregistruoti.
2. Pasirinkti autoservisą, tame autoservise dirbantį mechaniką, paslaugą iš sąrašų (sąrašai iš administracinės dalies) ir užsakyti paslaugą nurodant datą ir laiką.
3. Įvertinti mechaniką, kurio paslaugą užsisakė.
4. Autoservisus išrūšiuoti pagal pavadinimą.
5. Matyti mechanikų sąrašą, išrūšiuoti jį pagal įvertinimus ir nufiltruoti pagal autoservisą.
6. Matyti savo užsakymus ir jų statusą (ar patvirtintas, ar atšauktas). 
